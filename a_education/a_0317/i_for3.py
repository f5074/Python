message = "Hello!"
messages = ["Hello Python"]
numbers = (1,2,3)

for item in message:
    print("문자열 'Hello!'를 순차적으로 출력.")
    print(item)
print()


for item in messages:
    print("리스트 '[Hello Python]'를 순차적으로 출력.")
    print(item)
print()


for item in numbers:
    print("튜플 '(1,2,3)'를 순차적으로 출력.")
    print(item)
print()


